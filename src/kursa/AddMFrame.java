    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String message=jTextArea1.getText();
        String lessonN=jTextField1.getText();
        if(message.equals("") || lessonN.equals("")){
            JOptionPane.showMessageDialog(null, "Заполните все поля.", "Добавить материал", JOptionPane.DEFAULT_OPTION);
        }else{
            Task t=new Task();
            if(t.addMaterial(message,lessonN)){
                JOptionPane.showMessageDialog(null, "Текст успешно добавлен", "Текст", JOptionPane.INFORMATION_MESSAGE);
                jTextArea1.setText("");
            }else{
                JOptionPane.showMessageDialog(null, "Произошла ошибка. Попробуйте повторить операцию.", "Текст", JOptionPane.INFORMATION_MESSAGE);
                jTextArea1.setText("");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

