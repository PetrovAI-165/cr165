    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String p=jTextField1.getText();
        if(p.equals("")){
            JOptionPane.showMessageDialog(null, "Введите номер урока.", "Урок", JOptionPane.DEFAULT_OPTION);
        }else{
            File f1=new File("Lesson"+p+".txt");
            if(f1.exists()){
                Study st=new Study();
                String m=st.getMaterial(p);
                jTextArea1.setText(m);   
            }else{
                 JOptionPane.showMessageDialog(null, "Урок под номером "+p+" отсутствует.", "Материал", JOptionPane.DEFAULT_OPTION);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

