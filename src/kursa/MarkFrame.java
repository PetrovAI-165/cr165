    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name=jTextField1.getText();
        String surname=jTextField2.getText();
        if(name.equals("") || surname.equals("")){
            JOptionPane.showMessageDialog(null, "Заполните все поля.", "Узнать оценки", JOptionPane.DEFAULT_OPTION);
        }else{
            Registration re=new Registration();
            String login=re.getMark(name,surname);
            if(login.equals("")){
                JOptionPane.showMessageDialog(null, "Студент с такими фамилией и именем отсутствует.", "Оценки", JOptionPane.INFORMATION_MESSAGE);
            }else{
                File file1 = new File(login+".txt");
                if (file1.exists()) {
                    try {
                        BufferedReader reader = new BufferedReader(new FileReader(login+".txt"));
                        String line;
                        jTextArea1.append("\t"+name+" "+surname+":\r\n");
                        while ((line = reader.readLine()) != null) {
                                jTextArea1.append(line+"\r\n");
                        }
                        jTextArea1.append("_________________________________________\r\n");
                    } catch (IOException e) {
                        throw new RuntimeException(e);}
                }else{
                    JOptionPane.showMessageDialog(null, "Этот студент еще не отвечал на вопросы.", "Оценки", JOptionPane.INFORMATION_MESSAGE);
                }

            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

