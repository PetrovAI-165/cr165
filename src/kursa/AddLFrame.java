    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String l=jTextField1.getText();
        if(l.equals("")){
            JOptionPane.showMessageDialog(null, "Заполните все поля.", "Добавить урок", JOptionPane.DEFAULT_OPTION);
        }else{
            try {
                    FileWriter writer = new FileWriter("lessons.txt", true);
                    BufferedWriter bufferWriter = new BufferedWriter(writer);
                    bufferWriter.write(l + "\r\n");
                    bufferWriter.close();
                } catch (IOException e) {
                    System.out.println(e);
                }
            JOptionPane.showMessageDialog(null, "Урок успешно добавлен.", "Новый урок", JOptionPane.INFORMATION_MESSAGE);
            jTextField1.setText("");
        }    
    }//GEN-LAST:event_jButton1ActionPerformed

