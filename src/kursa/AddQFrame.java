    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String question=jTextField1.getText();
        String answer=jTextField2.getText();
        String num=jTextField3.getText();
        if(question.equals("") || answer.equals("") || num.equals("")){
            JOptionPane.showMessageDialog(null, "Заполните все поля.", "Добавить вопрос", JOptionPane.DEFAULT_OPTION);
        }else{
            Task t=new Task();
            if(t.addQuestion(question,num) && t.addAnswer(answer,num)){
                JOptionPane.showMessageDialog(null, "Текст успешно добавлен", "Текст", JOptionPane.INFORMATION_MESSAGE);
                jTextField1.setText("");
                jTextField2.setText("");
            }else{
                JOptionPane.showMessageDialog(null, "Произошла ошибка. Попробуйте повторить операцию.", "Текст", JOptionPane.INFORMATION_MESSAGE);
                jTextField1.setText("");
                jTextField2.setText("");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

